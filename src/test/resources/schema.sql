/*==============================================================*/
/* Table: appointment                                           */
/*==============================================================*/
CREATE TABLE appointment
(
   id          BIGINT(18) NOT NULL AUTO_INCREMENT,
   staff_id    BIGINT(18) NOT NULL,
   patient_id  BIGINT(18) NOT NULL,
   start_time  DATETIME   NOT NULL,
   end_time    DATETIME   NOT NULL,
   status_code BIGINT(18) NOT NULL,
   location    VARCHAR(100),
   symptom     VARCHAR(1000),
   vitals      VARCHAR(100),
   allergies   VARCHAR(200),
   follow_up   VARCHAR(1000),
   PRIMARY KEY (id)
);

/*==============================================================*/
/* Table: appt_status                                           */
/*==============================================================*/
CREATE TABLE appt_status
(
   id          BIGINT(18)  NOT NULL AUTO_INCREMENT,
   description VARCHAR(50) NOT NULL,
   PRIMARY KEY (id)
);

/*==============================================================*/
/* Table: department                                            */
/*==============================================================*/
CREATE TABLE department
(
   id   BIGINT(18)  NOT NULL AUTO_INCREMENT,
   name VARCHAR(50) NOT NULL,
   PRIMARY KEY (id)
);

/*==============================================================*/
/* Table: feedback                                              */
/*==============================================================*/
CREATE TABLE feedback
(
   id         BIGINT(18)    NOT NULL AUTO_INCREMENT,
   staff_id   BIGINT(18)    NOT NULL,
   patient_id BIGINT(18)    NOT NULL,
   time       DATETIME,
   score      NUMERIC(8, 2) NOT NULL,
   message    VARCHAR(500),
   appt_id    BIGINT(18)    NOT NULL,
   PRIMARY KEY (id)
);

/*==============================================================*/
/* Table: insurance                                             */
/*==============================================================*/
CREATE TABLE insurance
(
   id             BIGINT(18)  NOT NULL AUTO_INCREMENT,
   policy_id      VARCHAR(50) NOT NULL,
   company        VARCHAR(50) NOT NULL,
   effective_date DATE        NOT NULL,
   end_date       DATE,
   PRIMARY KEY (id)
);

/*==============================================================*/
/* Table: language                                              */
/*==============================================================*/
CREATE TABLE language
(
   id   BIGINT(18)  NOT NULL AUTO_INCREMENT,
   name VARCHAR(50) NOT NULL,
   PRIMARY KEY (id)
);

/*==============================================================*/
/* Table: medical_center                                        */
/*==============================================================*/
CREATE TABLE medical_center
(
   id      BIGINT(18)  NOT NULL AUTO_INCREMENT,
   name    VARCHAR(50) NOT NULL,
   address VARCHAR(100),
   PRIMARY KEY (id)
);

/*==============================================================*/
/* Table: medical_staff                                         */
/*==============================================================*/
CREATE TABLE medical_staff
(
   id                BIGINT(18)  NOT NULL AUTO_INCREMENT,
   userid            BIGINT(18)  NOT NULL,
   name              VARCHAR(50) NOT NULL,
   email             VARCHAR(50),
   speciality        VARCHAR(100),
   phone             VARCHAR(50),
   gender            CHAR(1),
   photo_url         VARCHAR(500),
   department_id     BIGINT(18),
   medical_center_id BIGINT(18),
   PRIMARY KEY (id)
);

/*==============================================================*/
/* Table: patient                                               */
/*==============================================================*/
CREATE TABLE patient
(
   id                      BIGINT(18)  NOT NULL AUTO_INCREMENT,
   userid                  BIGINT(18)  NOT NULL,
   pcp_id                  BIGINT(18),
   name                    VARCHAR(50) NOT NULL,
   email                   VARCHAR(50),
   address                 VARCHAR(100),
   phone                   VARCHAR(50),
   gender                  CHAR(1),
   race                    VARCHAR(50),
   photo_url               VARCHAR(500),
   dob                     DATE,
   emergency_contact_name  VARCHAR(50),
   emergency_contact_phone VARCHAR(20),
   insurance_id            BIGINT(18),
   communication_pref      VARCHAR(200),
   medical_pref            VARCHAR(200),
   staff_pref              VARCHAR(200),
   PRIMARY KEY (id)
);

/*==============================================================*/
/* Table: staff_workday_mapping                                 */
/*==============================================================*/
CREATE TABLE staff_workday_mapping
(
   id       BIGINT(18) NOT NULL AUTO_INCREMENT,
   staff_id BIGINT(18) NOT NULL,
   day_id   BIGINT(18) NOT NULL,
   PRIMARY KEY (id)
);

/*==============================================================*/
/* Table: user                                                  */
/*==============================================================*/
CREATE TABLE user
(
   id                BIGINT(18)  NOT NULL AUTO_INCREMENT,
   username          VARCHAR(50) NOT NULL,
   password          VARCHAR(50) NOT NULL,
   registration_date DATETIME    NOT NULL,
   last_login        DATETIME,
   role              CHAR(1)     NOT NULL,
   PRIMARY KEY (id)
);

/*==============================================================*/
/* Table: user_event_log                                        */
/*==============================================================*/
CREATE TABLE user_event_log
(
   id        BIGINT(18) NOT NULL AUTO_INCREMENT,
   timestamp DATETIME,
   user_id   BIGINT(18),
   ip        VARCHAR(50),
   agent     VARCHAR(100),
   operation VARCHAR(100),
   PRIMARY KEY (id)
);

/*==============================================================*/
/* Table: user_language_mapping                                 */
/*==============================================================*/
CREATE TABLE user_language_mapping
(
   id          BIGINT(18) NOT NULL AUTO_INCREMENT,
   language_id BIGINT(18) NOT NULL,
   user_id     BIGINT(18) NOT NULL,
   PRIMARY KEY (id)
);

/*==============================================================*/
/* Table: workday                                               */
/*==============================================================*/
CREATE TABLE workday
(
   id  BIGINT(18)  NOT NULL AUTO_INCREMENT,
   day VARCHAR(50) NOT NULL,
   PRIMARY KEY (id)
);

